bool checkWithGap(const uint8_t* src, const uint8_t* dst, size_t size)
{
    while (size--)
    {
        if (std::min(*src - *dst, *dst - *src) > 1)
            return false;
        ++dst, ++src;
    }
    return true;
}

void decodeAndCheck(
    const char* encodedData,
    size_t encodedSize,
    const char* rawData,
    size_t rawSize,
    AVCodecID codecId,
    AVPixelFormat pixelFormat)
{
    // Set up a compressed frame.
    nx::utils::ByteArray data(
        CL_MEDIA_ALIGNMENT,
        0,
        AV_INPUT_BUFFER_PADDING_SIZE); //< Memory will be corrupted by the decoder without padding.
    data.write(encodedData, encodedSize);
    QnCompressedVideoDataPtr video = std::make_shared<QnWritableCompressedVideoData>();
    video->setData(std::move(data));
    video->compressionType = codecId;
    video->flags = QnAbstractMediaData::MediaFlags_AVKey;
    video->timestamp = 1;
    video->context = QnFfmpegHelper::createVideoCodecParametersAnnexB(video.get());
    // Decode.
    DecoderConfig decoderConfig;
    auto decoder = std::make_unique<QnFfmpegVideoDecoder>(
        decoderConfig, nullptr, video);
    QSharedPointer<CLVideoDecoderOutput> decodedVideoFrame(new CLVideoDecoderOutput());
    ASSERT_TRUE(
        decoder->decode(video, &decodedVideoFrame)
        || decoder->decode(nullptr, &decodedVideoFrame)); //< Need to flush.
    // Scale.
    auto scaledVideo = decodedVideoFrame->scaled(decodedVideoFrame->size(), pixelFormat);
    auto dataToCheck = scaledVideo->rawData();
    ASSERT_EQ(dataToCheck.size(), rawSize);
    ASSERT_TRUE(
        checkWithGap((const uint8_t*) dataToCheck.constData(), (const uint8_t*) rawData, rawSize));
}

void scaleAndCheckRgb24(
    const char* srcData,
    size_t srcSize,
    const char* dstData,
    size_t dstSize,
    AVPixelFormat pixelFormat,
    QSize srcResolution,
    QSize dstResolution)
{
    QSharedPointer<CLVideoDecoderOutput> srcFrame(new CLVideoDecoderOutput());
    srcFrame->reallocate(srcResolution, pixelFormat);
    srcFrame->memZero();
    const int linesizeWithoutPadding = srcSize / srcResolution.height();
    ASSERT_TRUE(srcFrame->linesize[0] >= linesizeWithoutPadding);
    for (int i = 0; i < srcResolution.height(); ++i)
    {
        memcpy(
            srcFrame->data[0] + srcFrame->linesize[0] * i,
            srcData + i * linesizeWithoutPadding,
            linesizeWithoutPadding);
    }
    // Scale.
    auto scaledVideo = srcFrame->scaled(dstResolution, pixelFormat, /*keepAspectRatio*/ true, SWS_FAST_BILINEAR);
    auto dataToCheck = scaledVideo->rawData();
    ASSERT_EQ(dataToCheck.size(), dstSize);
    ASSERT_EQ(0, memcmp(dataToCheck.constData(), dstData, dstSize));
}

