#include "certificate_cache.h"

#include <nx/network/nx_network_ini.h>
#include <nx/network/ssl/certificate.h>
#include <nx/network/ssl/helpers.h>
#include <nx/utils/log/log.h>

namespace nx::vms::client::core {

struct CertificateCache::Private
{
    using CertificateType = CertificateVerifier::CertificateType;
    using Status = CertificateVerifier::Status;

    template<typename Chain>
    Status verifyCertificateByCache(
        const nx::Uuid& serverId,
        const nx::Url& url,
        const Chain& chain)
    {
        if (!NX_ASSERT(!chain.empty()))
            return Status::notFound;

        const auto& certificate = chain[0];

        const auto connectionCertificatePublicKey = connectionCertificate(serverId);
        const auto autoGeneratedCertificatePublicKey = autoGeneratedCertificate(serverId);

        if (!connectionCertificatePublicKey && !autoGeneratedCertificatePublicKey)
        {
            NX_VERBOSE(this, "Certificate public key was not found");
            return Status::notFound;
        }

        if (connectionCertificatePublicKey == certificate.publicKey())
        {
            NX_VERBOSE(this, "Certificate public key matches connection certificate");
            return Status::ok;
        }

        if (autoGeneratedCertificatePublicKey == certificate.publicKey())
        {
            NX_VERBOSE(this, "Certificate public key matches autogenerated certificate");
            return Status::ok;
        }

        auto hostName = url.host().toStdString();
        if (trustedCertificate(serverId, hostName) == certificate.publicKey())
        {
            NX_VERBOSE(this, "Certificate public key is trusted");
            return Status::ok;
        }

        std::string errorMessage;
        if (nx::network::ssl::verifyBySystemCertificates(chain, hostName, &errorMessage))
        {
            NX_VERBOSE(this, "Certificate public is trusted by OS check");
            trustedCertificates[std::make_pair(serverId, hostName)] = certificate.publicKey();
            return Status::ok;
        }

        NX_WARNING(this,
            "Certificate mismatch for the server %1. Cached: %2 and %3, actual: %4.\nOS check: %5, "
            "url: %6",
            serverId,
            connectionCertificatePublicKey,
            autoGeneratedCertificatePublicKey,
            certificate.publicKey(),
            errorMessage,
            url.toString());

        return Status::mismatch;
    }

    std::optional<std::string> connectionCertificate(const nx::Uuid& serverId) const
    {
        if (auto it = connectionCertificates.find(serverId);
            it != connectionCertificates.end())
        {
            return it->second;
        }
        return {};
    }

    std::optional<std::string> autoGeneratedCertificate(const nx::Uuid& serverId) const
    {
        if (auto it = autoGeneratedCertificates.find(serverId);
            it != autoGeneratedCertificates.end())
        {
            return it->second;
        }
        return {};
    }

    std::optional<std::string> trustedCertificate(
        const nx::Uuid& serverId, const std::string& hostName) const
    {
        if (auto it = trustedCertificates.find(std::make_pair(serverId, hostName));
            it != trustedCertificates.end())
        {
            return it->second;
        }
        return {};
    }

    mutable Mutex mutex;
    std::map<nx::Uuid, std::string> connectionCertificates;
    std::map<nx::Uuid, std::string> autoGeneratedCertificates;
    std::map<std::pair<nx::Uuid, std::string>, std::string> trustedCertificates;
};

CertificateCache::CertificateCache(QObject* parent):
    base_type(parent),
    d(new Private())
{
}

CertificateCache::~CertificateCache()
{
}

void CertificateCache::addCertificate(
    const nx::Uuid& serverId,
    const std::string& publicKey,
    CertificateVerifier::CertificateType certType)
{
    NX_MUTEX_LOCKER lock(&d->mutex);

    switch (certType)
    {
        case CertificateVerifier::CertificateType::autogenerated:
            d->autoGeneratedCertificates[serverId] = publicKey;
            return;
        case CertificateVerifier::CertificateType::connection:
            d->connectionCertificates[serverId] = publicKey;
            return;
    }
    NX_ASSERT(false, "Unknown certificate type %1", (int) certType);
}

std::optional<std::string> CertificateCache::certificate(
    const nx::Uuid& serverId,
    CertificateVerifier::CertificateType certType) const
{
    NX_MUTEX_LOCKER lock(&d->mutex);

    switch (certType)
    {
        case CertificateVerifier::CertificateType::autogenerated:
            return d->autoGeneratedCertificate(serverId);
        case CertificateVerifier::CertificateType::connection:
            return d->connectionCertificate(serverId);
        default:
            NX_ASSERT(false, "Unknown certificate type");
            return {};
    }
}

void CertificateCache::removeCertificates(const nx::Uuid& serverId)
{
    NX_MUTEX_LOCKER lock(&d->mutex);

    d->connectionCertificates.erase(serverId);
    d->autoGeneratedCertificates.erase(serverId);
    std::erase_if(d->trustedCertificates,
        [serverId](const auto& item)
        {
            auto const& [key, value] = item;
            return key.first == serverId;
        });
}

nx::network::ssl::AdapterFunc CertificateCache::makeAdapterFunc(
    const nx::Uuid& serverId, const nx::Url& url)
{
    if (!nx::network::ini().verifyVmsSslCertificates)
        return nx::network::ssl::kAcceptAnyCertificate;

    if (!NX_ASSERT(!serverId.isNull()))
        return nx::network::ssl::kRejectAnyCertificate;

    return nx::network::ssl::makeAdapterFunc(
        [ptr = std::weak_ptr<Private>(d), serverId, url](auto chain)
        {
            auto d = ptr.lock();
            if (!d)
            {
                NX_DEBUG(NX_SCOPE_TAG, "Certificate verification skipped: cache is already destroyed");
                return false;
            }

            NX_MUTEX_LOCKER lock(&d->mutex);
            return d->verifyCertificateByCache(serverId, url, chain) == CertificateVerifier::Status::ok;
        }
    );
}

} // namespace nx::vms::client::core
